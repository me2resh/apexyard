  apply(lang) {
    const pack = this.strings[lang] || this.strings.ar;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (!key || pack[key] == null) return;
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = pack[key];
      } else if (el.tagName === "IMG") {
        el.alt = pack[key];
      } else {
        el.textContent = pack[key];
      }
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (pack[key] != null) el.setAttribute("placeholder", pack[key]);
    });
    document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
      const key = el.getAttribute("data-i18n-aria-label");
      if (pack[key] != null) el.setAttribute("aria-label", pack[key]);
    });
    const titleKey = document.body.dataset.pageTitle;
    if (titleKey && pack[titleKey]) document.title = pack[titleKey];
    const desc = document.querySelector('meta[name="description"]');
    const descKey = document.body.dataset.pageDesc;
    if (desc && descKey && pack[descKey]) desc.setAttribute("content", pack[descKey]);
    document.querySelectorAll("[data-mobile-col]").forEach((el) => {
      const colKey = el.getAttribute("data-mobile-col");
      if (colKey && pack[colKey] != null) el.dataset.mobileLabel = pack[colKey];
    });
    document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
      btn.classList.toggle("active", btn.dataset.langBtn === lang);
      btn.setAttribute("aria-pressed", btn.dataset.langBtn === lang ? "true" : "false");
    });
    try {
      localStorage.setItem("blendavit-lang", lang);
    } catch (_) {}
    document.dispatchEvent(new CustomEvent("blendavit:lang", { detail: { lang } }));
  },

  init() {
    let lang = this.defaultLang;
    try {
      const stored = localStorage.getItem("blendavit-lang");
      if (stored === "en" || stored === "ar") lang = stored;
    } catch (_) {}
    this.apply(lang);
    document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
      btn.addEventListener("click", () => this.apply(btn.dataset.langBtn));
    });
  },
